# integer
a=21
print(type(a)) 

# float
a=21.99
print(type(a))
a=2.00+32
print(type(a))

# complex
a=2+3j
print(type(a))


#-----sequence datatype

# list
city=['chennai','trichy','perambalur','tanjavur','madurai']
print(type(city))

# tuple
city=('chennai','trichy','perambalur','tanjavur','madurai')
print(type(city))

# string
name="logesh"
print(type(name))

#----------- boolean

active=True
print(type(active))

# set (unordered collection)

city={'chennai','trichy','perambalur','tanjavur','madurai'}
print(city)
print(type(city))

# dictionary (ordered collection)
city={'naame':'logesh','id':'4565'}
print(city)
print(type(city))

